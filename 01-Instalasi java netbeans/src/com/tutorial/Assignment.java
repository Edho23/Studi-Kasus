package com.tutorial;


public class Assignment {
    public static void main(String[] args) {
        // Operator Assignment
        
        // Assigmeent operator jumlah
        int a = 1;
        
        a += 10; // a = a + 10;
        
        System.out.println("Nilai a = " + a);
        
        // Assigmeent operator pengurangan
        int b = 100;
        
        b -= 25; // b = b - 25;
        
        System.out.println("Nilai a = " + b);
    }
    
}
